window.m360PopulateProvinces = function (provinceSelect) {
  if (!provinceSelect || !window.M360_IRAN_LOCATIONS) return;
  provinceSelect.innerHTML = '<option value="">انتخاب استان</option>';
  Object.keys(window.M360_IRAN_LOCATIONS).forEach(function (province) {
    var opt = document.createElement('option');
    opt.value = province;
    opt.textContent = province;
    provinceSelect.appendChild(opt);
  });
};

window.m360PopulateCities = function (provinceSelect, citySelect) {
  if (!provinceSelect || !citySelect) return;
  var province = provinceSelect.value;
  citySelect.innerHTML = '<option value="">انتخاب شهر</option>';
  if (!province || !window.M360_IRAN_LOCATIONS[province]) {
    citySelect.disabled = true;
    return;
  }
  citySelect.disabled = false;
  window.M360_IRAN_LOCATIONS[province].forEach(function (city) {
    var opt = document.createElement('option');
    opt.value = city;
    opt.textContent = city;
    citySelect.appendChild(opt);
  });
};
